const nightMarket = document.querySelector('.nightmarket');
const btnsContainer = document.querySelector('.button-container');

function showItems(array) {
  let data = array
    .map(item => {
      return `
    <div class="item">
        <div class="img">
          <img
            src="${item.cover_image}"
            alt="${item.title}"
          />
        </div>
        <ul class="content">
          <li>Name: <span>${item.title}</span></li>
          <li>
            Address: <span>${item.address}</span>
          </li>
          <li>Time: <span>${item.business_hours}</span></li>
          <li>
            Guide: <span>${item.traffic_guideline}</span>
          </li>
        </ul>
      </div>
    `;
    })
    .join('');
  nightMarket.innerHTML = data;
}

window.addEventListener('load', () => {
  //Show NightMarket
  showItems(dataBase);

  //Show Total Place
  const showBtns = dataBase.reduce(
    (values, item) => {
      if (!values.includes(item.address.substr(0, 6))) {
        values.push(item.address.substr(0, 6));
      }
      return values;
    },
    ['All']
  );
  const placeBtns = showBtns
    .map(item => {
      // console.log(item);
      return `
    <button class="filter-button" data-place="${item}">${item}</button>
    `;
    })
    .join('');
  btnsContainer.innerHTML = placeBtns;

  const filterBtns = document.querySelectorAll('.filter-button');

  //ButtonHandler
  filterBtns.forEach(btn => {
    btn.addEventListener('click', () => {
      const btnPlace = btn.dataset.place;
      const marketPlace = dataBase.filter(item => {
        if (item.address.substr(0, 6) == btnPlace) {
          return item;
        }
      });
      if (btnPlace == 'All') {
        showItems(dataBase);
      } else {
        showItems(marketPlace);
      }
    });
  });
});

window.addEventListener('scroll', () => {
  if (window.scrollY > 160) {
    btnsContainer.style.position = 'fixed';
    btnsContainer.style.flexDirection = 'column';
  } else {
    btnsContainer.style.position = 'relative';
    btnsContainer.style.flexDirection = 'row';
  }
});
